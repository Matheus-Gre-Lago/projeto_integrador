    private void buttonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEditarActionPerformed
            
        ServicosUsuario usuarioServicos = ServicosFactory.getUsuarioServicos();
        ModelUsuario modelUsuario = new ModelUsuario();
        
        if(buttonEditar.getText().equals("Editar")){
            textFieldNome.setText("matheus");
            textFieldUser.setText("matheus");
            textFieldEmail.setText("matheus@gmail");
            textFieldSenha.setText("");
            textFieldTelefone.setText("1111111111");
            
            buttonEditar.setText("Salvar");
        }else{
            modelUsuario.setIdUsuario(1);
            modelUsuario.setNome(textFieldNome.getText());
            modelUsuario.setUser(textFieldUser.getText());
            modelUsuario.setEmail(textFieldEmail.getText());
            modelUsuario.setSenha(textFieldSenha.getText());
            modelUsuario.setTelefone(textFieldTelefone.getText());
            
            try {
                usuarioServicos.atualizarUsuarioBD(modelUsuario);
            } catch (SQLException ex) {
                Logger.getLogger(UsuarioEditarExcluir.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            buttonEditar.setText("Editar");
        }
          
    }//GEN-LAST:event_buttonEditarActionPerformed

